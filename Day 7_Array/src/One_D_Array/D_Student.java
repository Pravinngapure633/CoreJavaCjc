package One_D_Array;

public class D_Student {
	
		private int rollno;
		private String name;
		private String address;
		
		//Constructor
		public D_Student(int rollno, String name, String address) {
		
			this.rollno = rollno;
			this.name = name;
			this.address = address;
		}

		public int getRollno() {
			return rollno;
		}

		public String getName() {
			return name;
		}

		public String getAddress() {
			return address;
		}
		
		
		

}
