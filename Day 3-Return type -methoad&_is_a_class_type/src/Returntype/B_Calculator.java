package Returntype;

public class B_Calculator {

			 
		// return type with parameterized
		public int Addition(int x, int y)
		{
			int result=x+y;
			return result;
			
		}
		
		public int Subtraction(int x , int y)
		{
			int result=x-y;
			return result;
				
		}
		public int Multiplication(int x , int y)
		{
			int result=x*y;
			return result;
				
		}
		public int Division(int x , int y)
		{
			int result=x/y;
			return result;
				
		}
}
