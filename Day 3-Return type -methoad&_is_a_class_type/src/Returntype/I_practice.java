package Returntype;

import java.util.Scanner;

public class I_practice {
		
		
		Scanner sc = new Scanner(System.in);
		
		

		public float Add()
		{
			int x=sc.nextInt();
			int y=sc.nextInt();
			float result=x+y;
			return result;
		}
		public double Sub()
		{	
			int x=sc.nextInt();
			int y=sc.nextInt();
			double result=x-y;
			return result;
		}
}
